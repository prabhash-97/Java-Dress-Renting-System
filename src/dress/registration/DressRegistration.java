    public void autoID(){
    
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/db_dressrent","root","");
            Statement s = conn.createStatement();
            
            ResultSet rs = s.executeQuery("select Max(Dres_id) from dress_reg");
            rs.next();
            rs.getString("Max(Dres_id)");
            
            if(rs.getString("Max(Dres_id)") == null){
                dressId.setText("D0001");
            }
            else{
                long id = Long.parseLong(rs.getString("Max(Dres_id)").substring(2,rs.getString("Max(Dres_id)").length()));
                id++;  
                
                dressId.setText("D0" + String.format("%03d", id));
            }
            
            
        } catch(Exception e){
           JOptionPane.showMessageDialog(null , e);
        }
    }
