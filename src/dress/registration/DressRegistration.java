    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
         
          int selectedIndex = jTable1.getSelectedRow();
          
          dressId.setText(df.getValueAt(selectedIndex, 0).toString());
           dressSize.setSelectedItem(df.getValueAt(selectedIndex, 1).toString());
          dressType.setSelectedItem(df.getValueAt(selectedIndex, 2).toString());
          dressColor.setText(df.getValueAt(selectedIndex, 3).toString());
          String avilable = df.getValueAt(selectedIndex,4).toString();
          if(avilable.equals("Yes")){
              Yes.setSelected(true);
          }else{
             No.setSelected(true); 
          }
         
          buttonAdd.setEnabled(false);
          buttonEdit.setEnabled(true);
          buttonDelete.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

