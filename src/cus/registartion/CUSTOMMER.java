        public  void table_update(){
    
        int c;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_dressrent","root","");
            pst = con.prepareStatement("select * from cus_reg");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            c = rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
            
                Vector v2 = new Vector();
                
                for(int i =0; i<=c; i++){
                    v2.add(rs.getString("Id"));
                    v2.add(rs.getString("Cus_id"));
                    v2.add(rs.getString("Cus_name"));
                    v2.add(rs.getString("Cus_add"));
                    v2.add(rs.getString("Cus_mob"));  
                }
                df.addRow(v2);
            }
   
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CUSTOMMER.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
    
    
    
    
    
    public void autoregid(){
    
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/db_dressrent","root","");
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("select Max(Id) from cus_reg");
            rs.next();
            rs.getString("Max(Id)");
            
            if(rs.getString("Max(Id)") == null){
                regid.setText("C0001");
            }
            else{
                long id = Long.parseLong(rs.getString("Max(Id)").substring(2,rs.getString("Max(Id)").length()));
                id++;  
                
                regid.setText("C0" + String.format("%03d", id));
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CUSTOMMER.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
